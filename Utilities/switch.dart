void main() {
  String cars = "BMW";

  switch (cars) {
    case "Volvo":
      print("You have choosen Volvo");
      break;

    case "BMW":
      print("You have choosen BMW");
      break;

    case "Nissan Note":
      print("You have choosen Nissan Note");
      break;

    case "Ford Ranger":
      print("You have choosen Ford Ranger");
      break;

    default:
      print("The value is out of range");
      break;
  }
}
